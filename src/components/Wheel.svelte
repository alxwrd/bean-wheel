<script>
  import Winwheel from "./Winwheel.js";
  import { createEventDispatcher } from "svelte";
  import { onMount } from "svelte";

  const dispatch = createEventDispatcher();

  let wheel;

  let spinning = false;

  let click = new Audio("sounds/click.mp3");
  let tune = new Audio("sounds/Carroll_Park_Sting.mp3")

  export let width = "500";
  export let height = "500";

  onMount(() => {
    fetch("https://api.jsonbin.io/b/5fbe65434f12502c21d86700/latest")
      .then(response => {
        return response.json();
      })
      .then(json => {
        wheel = new Winwheel({
          canvasId: "wheel",
          pointerAngle: 90,
          numSegments: json.segments.length,
          innerRadius: 100, // Set inner radius to make wheel hollow.
          textFontSize: 14, // Set font size accordingly.
          textMargin: 0, // Take out default margin.
          // Define segments including colour and text.
          segments: json.segments,
          // Define spin to stop animation.
          animation: {
            type: "spinToStop",
            duration: 6,
            spins: 5,
            easing: "back.out(0.2)",
            // callbackSound: playSound,
            callbackFinished: wheelStopped
          }
        });
      });
  });

  function wheelStopped(indicatedSegment) {
    spinning = false;
    dispatch("message", {
      text: indicatedSegment.text
    });
    wheel.rotationAngle = 0;
  }

  function playSound() {
    let click = new Audio("sounds/click.mp3");
    click.play();
  }

  function spin() {
    if (spinning) {
      return;
    }
    spinning = true;
    tune.pause();
    tune.currentTime = 0;
    tune.play();
    wheel.startAnimation();
  }
</script>

<div class="text-center p-4 mx-auto">
  <canvas id="wheel" {width} {height} class="w-full">
    Canvas not supported, use another browser.
  </canvas>

  <div
    class="block relative text-2xl"
    style="top: -50%; right: -50%; transform:translateY(90%);">
    <span>ðŸ‘ˆ</span>
  </div>

  <button
    class="bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 border
    border-blue-700 rounded m-4"
    on:click={spin}>
    SPIN
  </button>
</div>
